package io.github.degdev.engine.common.persistence;

import jakarta.persistence.Column;
import jakarta.persistence.EntityListeners;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.MappedSuperclass;
import jakarta.persistence.Version;
import java.time.Instant;
import lombok.Getter;
import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.jpa.domain.support.AuditingEntityListener;

/**
 * Base class for every persistent engine entity.
 *
 * <p>Provides a {@code Long} identity primary key ({@link GenerationType#IDENTITY}), an optimistic
 * locking {@link Version} field, and creation/modification timestamps populated by JPA auditing.
 * User audit ({@code created_by}/{@code updated_by}) is deferred to milestone {@code 02-auth}, when
 * a principal first exists.
 *
 * <p>Read-only by design: {@code @Getter} exposes the managed fields, but there are no setters —
 * the id is database-generated, the version is managed by the JPA provider, and the timestamps are
 * filled by auditing. Equality is defined by the concrete entity's business key, not here.
 */
@Getter
@MappedSuperclass
@EntityListeners(AuditingEntityListener.class)
public abstract class BaseEntity {

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  private Long id;

  @Version private Long version;

  @CreatedDate
  @Column(name = "created_at", nullable = false, updatable = false)
  private Instant createdAt;

  @LastModifiedDate
  @Column(name = "updated_at", nullable = false)
  private Instant updatedAt;
}
